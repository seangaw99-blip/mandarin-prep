#!/usr/bin/env node
/**
 * Scans public/images/vocab/ and writes a TS module listing the available
 * image IDs, so the Picture Cards UI only shows words that actually have art.
 * Run this after generating new images, before committing.
 */
import { readdir, writeFile } from 'node:fs/promises';
import { dirname, join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');
const dir = join(root, 'public', 'images', 'vocab');
const out = join(root, 'src', 'data', 'vocab-image-manifest.ts');

const files = await readdir(dir);
const ids = files
  .filter((f) => f.endsWith('.jpg'))
  .map((f) => f.replace(/\.jpg$/, ''))
  .sort();

const body = `// AUTO-GENERATED by scripts/build-vocab-manifest.mjs — do not edit by hand.
export const VOCAB_IMAGE_IDS: ReadonlySet<string> = new Set([
${ids.map((id) => `  '${id}',`).join('\n')}
]);
`;

await writeFile(out, body);
console.log(`Wrote ${ids.length} image IDs to ${out}`);
